let userName = "";

function showLoading() {
  userName = document.getElementById("name").value;
  const e = document.getElementById("email").value;

  if (!userName || !e) {
    Notiflix.Notify.failure("Please enter both name and email.");
    return;
  }

  sessionStorage.setItem("userName", userName);
  sessionStorage.setItem("userEmail", e);

  // Show animated loading spinner with user greeting
  Notiflix.Loading.circle(`Finding your certificate, ${userName}...`);

  setTimeout(() => {
    Notiflix.Loading.remove();
    displayOfferLetters();
    showWelcomeMessage();
  }, 2000);
}

function displayOfferLetters() {
  const normalizedUserName = userName.trim().toLowerCase(); // Trim spaces and convert to lowercase
  const nameKey = Object.keys(nameInfoMapping).find(
    key => key.trim().toLowerCase() === normalizedUserName
  );

  const t = document.getElementById("offerLetterGrid");
  t.innerHTML = ""; // Clear previous content

  if (nameKey) {
    const { image } = nameInfoMapping[nameKey];

    const fileCard = document.createElement("div");
    fileCard.classList.add("file-card");
    fileCard.style.animation = "fadeIn 0.5s ease-in-out"; // Smooth fade-in animation

    const img = document.createElement("img");
    img.src = image;
    img.alt = `${nameKey}'s Completion Certificate`;
    img.onclick = () => openModal(image);

    // Download button
    const downloadBtn = document.createElement("button");
    downloadBtn.classList.add("download-btn");
    downloadBtn.innerText = "Download Certificate";
    downloadBtn.onclick = () => downloadImage(image, `${nameKey}_Offer_Letter.jpg`);

    // Share on LinkedIn button (initially hidden)
    const shareBtn = document.createElement("button");
    shareBtn.id = "linkedin-share-btn";
    shareBtn.classList.add("share-btn");
    shareBtn.style.display = "none"; // Hide it initially
    shareBtn.style.backgroundColor = "#0077B5"; // LinkedIn blue
    shareBtn.style.marginTop = "10px";
    shareBtn.onclick = () => {
        const certificateImageURL = image; // The URL of the user's certificate image
        const title = "Internship Completion at DC Cloud Solutions";
        const summary = `I'm excited to share that I have successfully completed my internship at DC Cloud Solutions! It was a great learning experience. #DC Cloud Solutions #Internship #WebDevelopment #Certificate`;

        // Create a special URL for LinkedIn to scrape the correct image.
        // We use a proxy service (metascraper.io) to generate a page with the correct Open Graph tags.
        // This ensures the certificate image appears in the share preview.
        const sharerUrl = `https://metascraper.io/?url=${encodeURIComponent(certificateImageURL)}&title=${encodeURIComponent(title)}&description=${encodeURIComponent(summary)}`;

        const linkedInUrl = `https://www.linkedin.com/sharing/share-offsite/?url=${encodeURIComponent(sharerUrl)}`;
        window.open(linkedInUrl, '_blank');
    };

    // Add elements to card
    fileCard.appendChild(img);
    fileCard.appendChild(downloadBtn);
    fileCard.appendChild(shareBtn); // Add the share button to the card
    t.appendChild(fileCard);

    t.style.display = "grid";
    document.getElementById("logout-btn").style.display = "block";
  } else {
    Notiflix.Notify.failure("No certificate found for this user. Please check your name and email.");
  }
}

function downloadImage(url, filename) {
  Notiflix.Loading.standard("Preparing download...");
  fetch(url)
    .then(response => response.blob())
    .then(blob => {
      Notiflix.Loading.remove();
      const link = document.createElement("a");
      link.href = URL.createObjectURL(blob);
      link.download = filename;
      link.click();
      URL.revokeObjectURL(link.href);

      // Show the LinkedIn share button after download starts
      const shareBtn = document.getElementById("linkedin-share-btn");
      if (shareBtn) {
          shareBtn.style.display = "block";
      }
    })
    .catch(error => {
      Notiflix.Loading.remove();
      Notiflix.Notify.failure("Download failed. Please try again.");
      console.error("Download failed:", error);
    });
}

