document.addEventListener("DOMContentLoaded", () => {
  const SpeechRecognition = window.SpeechRecognition || window.webkitSpeechRecognition;
  const isSpeechSupported = SpeechRecognition;

  const nameInput = document.getElementById("name");
  const emailInput = document.getElementById("email");

  if (!nameInput || !emailInput) return;

  function createVoiceButton() {

    const voiceBtn = document.createElement("button");
    voiceBtn.type = "button";
    voiceBtn.title = "Fill form with your voice";
    voiceBtn.innerHTML = "🎤";
    Object.assign(voiceBtn.style, {
        background: "#5a8dee",
        color: "white",
        border: "none",
        borderRadius: "50%",
        width: "38px",
        height: "38px",
        fontSize: "1.3em",
        marginLeft: "8px",
        cursor: "pointer",
        transition: "background 0.3s",
        display: "flex",
        alignItems: "center",
        justifyContent: "center",
    });

    // Place it after the name input
    nameInput.parentNode.style.display = "flex";
    nameInput.parentNode.style.alignItems = "center";
    nameInput.parentNode.insertBefore(voiceBtn, nameInput.nextSibling);

    if (!isSpeechSupported) {
        voiceBtn.disabled = true;
        voiceBtn.title = "Voice input not supported in this browser.";
        voiceBtn.style.opacity = "0.5";
    }

    return voiceBtn;
  }

  function createHelpButton() {
    const helpBtn = document.createElement("button");
    helpBtn.type = "button";
    helpBtn.title = "How to use voice input";
    helpBtn.innerHTML = "❓";
    Object.assign(helpBtn.style, {
        background: "transparent",
        color: "#5a8dee",
        border: "none",
        fontSize: "1.5em",
        marginLeft: "4px",
        cursor: "pointer",
    });

    helpBtn.onclick = () => {
        Notiflix.Report.info(
            'How Voice Fill Works',
            `
            <div style="text-align: left; line-height: 1.6;">
                <p>Follow these simple steps for a hands-free experience:</p>
                <ol style="padding-left: 20px; margin-top: 10px;">
                    <li>Click the <strong>microphone (🎤)</strong> button.</li>
                    <li>When prompted, clearly say your <strong>full name</strong>.</li>
                    <li>Next, when prompted, say your <strong>email address</strong>.</li>
                    <li>The form will automatically fill, submit, and start your download!</li>
                </ol>
                <p><strong>Voice Commands for Email:</strong></p>
                <ul style="padding-left: 20px;">
                    <li>For <strong>@</strong>, say "at" or "at the rate".</li>
                    <li>For <strong>.</strong>, say "dot".</li>
                    <li>For <strong>_</strong>, say "underscore".</li>
                    <li>For <strong>-</strong>, say "dash" or "minus".</li>
                </ul>
            </div>
            `,
            'Got It'
        );
    };

    // Place it after the name input's parent container
    nameInput.parentNode.appendChild(helpBtn);
  }

  const voiceBtn = createVoiceButton();

  if (isSpeechSupported && voiceBtn) {
    const recognition = new SpeechRecognition();
    recognition.lang = "en-US";
    recognition.interimResults = false;
    recognition.maxAlternatives = 1;

    let recognitionStep = null; // Can be 'name' or 'email'

    voiceBtn.onclick = () => {
      recognitionStep = 'name';
      Notiflix.Notify.info("Please say your full name...", { timeout: 4000 });
      voiceBtn.style.background = "#2c8c47"; // Green when recording
      voiceBtn.innerHTML = "🎙️";
      recognition.start();
    };

    recognition.onresult = (event) => {
      let transcript = event.results[0][0].transcript.trim();

      if (recognitionStep === 'name') {
        nameInput.value = transcript.replace(/\.$/, ''); // Remove trailing period if any
      } else if (recognitionStep === 'email') {
        // Process email transcript to handle spoken symbols
        let processedEmail = transcript.toLowerCase()
          .replace(/ at the rate | at /g, '@') // "at the rate" or "at" -> @
          .replace(/ dot /g, '.')             // "dot" -> .
          .replace(/ underscore /g, '_')      // "underscore" -> _
          .replace(/ dash | minus /g, '-');   // "dash" or "minus" -> -

        // Remove all remaining spaces and handle "attherate" if spoken as one word
        processedEmail = processedEmail.replace(/\s/g, '').replace(/attherate/g, '@');

        emailInput.value = processedEmail;
      }
    };

    recognition.onerror = (event) => {
      console.error("Speech recognition error:", event.error);
      Notiflix.Notify.failure("Sorry, I couldn't recognize your voice. Please try again.");
      recognitionStep = null; // Reset on error
    };

    recognition.onend = () => {
      if (recognitionStep === 'name') {
        // Finished getting name, now get email
        recognitionStep = 'email';
        Notiflix.Notify.info("Great! Now, please say your email address...", { timeout: 4000 });
        recognition.start(); // Listen again for the email
      } else if (recognitionStep === 'email') {
        // Finished getting email, reset button and submit
        voiceBtn.style.background = "#5a8dee"; // Back to default color
        voiceBtn.innerHTML = "🎤";
        recognitionStep = null;
        
        // Auto-submit and download
        showLoading();
        setTimeout(() => { // Wait for certificate to appear
          const downloadBtn = document.querySelector('.download-btn');
          if (downloadBtn) {
            downloadBtn.click();
          }
        }, 2500);
      }
    };
  }

  createHelpButton(); // Add the help button to the UI
});
